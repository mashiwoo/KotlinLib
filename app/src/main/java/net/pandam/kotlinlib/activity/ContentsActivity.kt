class ContentsActivity : AppCompatActivity(), NavigationView.OnNavigationItemSelectedListener {

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContentView(R.layout.activity_contents)
        setSupportActionBar(toolbar)

        fab.setOnClickListener { view ->
            Snackbar.make(view, "Replace with your own action", Snackbar.LENGTH_LONG)
                    .setAction("Action", null).show()
        }

        val toggle = ActionBarDrawerToggle(
                this, drawer_layout, toolbar, R.string.navigation_drawer_open, R.string.navigation_drawer_close)
        drawer_layout.addDrawerListener(toggle)
        toggle.syncState()

        nav_view.setNavigationItemSelectedListener(this)

        val adapter = BannerPagerAdapter(supportFragmentManager)

        updateHandler.postDelayed(runnable, 1000)
    }

    val updateHandler = Handler()

    val runnable = Runnable {
        bannerNumber++
        updateDisplay() // some action(s)
    }

    var bannerNumber: Int = 0

    private fun updateDisplay() {
        val tvBanner: TextView = findViewById(R.id.tvBanner) as TextView
        tvBanner.setText("Sample" + bannerNumber.toString())

        updateHandler.postDelayed(runnable, 1000)
    }

    override fun onBackPressed() {
        if (drawer_layout.isDrawerOpen(GravityCompat.START)) {
            drawer_layout.closeDrawer(GravityCompat.START)
        } else {
            super.onBackPressed()
        }
    }

    override fun onCreateOptionsMenu(menu: Menu): Boolean {
        // Inflate the menu; this adds items to the action bar if it is present.
        menuInflater.inflate(R.menu.contents, menu)
        return true
    }

